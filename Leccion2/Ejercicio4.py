# EJERCICIO 4: Etapas de vida
edad = int(input('Digite su edad: '))
if 0 <= edad < 10:
    print('La infancia es increible y bella')
elif 10 <= edad < 20:
    print('Tienes muchos cambios. mucho que estudiar')
elif 20 <= edad < 30:
    print('Amor y comienza el trabajo')
elif 30 <= edad < 40:
    print('Prosperidad')
elif 40 <= edad < 50:
    print('Cansancio')
elif 50 <= edad < 60:
    print('Pasas')
elif 60 <= edad < 70:
    print('Pasas y cansado')
elif 70 <= edad < 80:
    print('Llego lejos')
elif 80 <= edad < 90:
    print('Alto aguante')
else:
    print('Hora de palmar')
